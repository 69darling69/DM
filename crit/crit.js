function RandomCrit()
{
    let i = 1;
    while (i == 1)
    {
        i = Math.floor(Math.random() * crit.length);
    }
    random.innerHTML = "<td>" + crit[i]["Успех"] + "</td><td>" + crit[i]["ОписаниеУ"] + "</td><td>" + (i+2) + "</td><td>" + crit[i]["Провал"] + "</td><td>" + crit[i]["ОписаниеП"] + "</td>";
}

